/* https://javl.github.io/image2cpp/

convert them from plain bytes to a 32 x 4 byte 2d-array like:

{
	{ 0x00, 0x00, 0x00, 0x00 },
	...	 
	{ 0x00, 0x00, 0x00, 0x00 }
}
*/

import fs from "fs";

const input = `
0x00, 0x00, 0x00, 0x00, 0x7f, 0xfb, 0xfd, 0xfe, 0x7f, 0xf7, 0xfb, 0xfe, 0x7f, 0xef, 0xf7, 0xfe, 
0x7e, 0x00, 0x00, 0x7e, 0x7d, 0xff, 0xff, 0xbe, 0x7b, 0xf0, 0x0f, 0xde, 0x77, 0xef, 0xf7, 0xee, 
0x77, 0xf0, 0x0f, 0xee, 0x77, 0xff, 0xff, 0xee, 0x66, 0x00, 0x00, 0x66, 0x65, 0xff, 0xff, 0xa6, 
0x65, 0xff, 0xff, 0xa6, 0x65, 0xff, 0xff, 0xa6, 0x65, 0xff, 0xff, 0xa6, 0x75, 0xff, 0xff, 0xae, 
0x75, 0xff, 0xff, 0xae, 0x75, 0xff, 0xff, 0xae, 0x75, 0xff, 0xff, 0xae, 0x75, 0xff, 0xff, 0xae, 
0x76, 0x00, 0x00, 0x6e, 0x77, 0xff, 0xff, 0xee, 0x76, 0x7f, 0xfe, 0x6e, 0x75, 0xbf, 0xfd, 0xae, 
0x76, 0x7f, 0xfe, 0x6e, 0x77, 0xff, 0xff, 0xee, 0x77, 0xff, 0xff, 0xee, 0x70, 0x00, 0x00, 0x0e, 
0x7d, 0xbf, 0xfd, 0xbe, 0x7c, 0x3f, 0xfc, 0x3e, 0x7f, 0xff, 0xff, 0xfe, 0x00, 0x00, 0x00, 0x00
`;



// extract all hex numbers
const bytes = input.match(/0x[0-9a-fA-F]+/g);

// group into rows of 4
const rows = [];
for (let i = 0; i < bytes.length; i += 4) {
  rows.push(bytes.slice(i, i + 4));
}

// format output
const output = `{
${rows
  .map(row => `\t{ ${row.join(", ")} }`)
  .join(",\n")}
}`;

console.log(output);

fs.writeFileSync("./output.txt", output);